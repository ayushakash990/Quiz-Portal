    public void setTimem(int Minutes, int Seconds){
        if(Minutes!=-1)
            time.setText(Minutes + ":" + Seconds);
        else{
            try{
            ObjectOutputStream objectOutputStream =new ObjectOutputStream(socket.getOutputStream());
            String[] s = {"sectioncompletemcqm",qcode,username,mcqmscore+"","0"};
            objectOutputStream.writeObject(s);
            sectionStudent.flagm = 1;
            sectionStudent.m1.setVisible(false);
            dispose();
            }catch(IOException e){
            e.printStackTrace();
            }
            JOptionPane.showMessageDialog(null, "Time-Over!");
            count = -1;
            ttm.stop();
        }
        
    }
    private void submitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_submitMouseClicked
        // TODO add your handling code here:
        String userans="";
           if(opt1.isSelected())
               userans+="A";
           if(opt2.isSelected())
               userans+="B";
           if(opt3.isSelected())
               userans+="C";
           if(opt4.isSelected())
               userans+="D";
           if(userans.equals(questionmcqm[currentmcqm][5])){
                mcqmscore++;
           }        
           currentmcqm++;
           if(currentmcqm!=totalquestionsmcqm)
           {
               question.setText(questionmcqm[currentmcqm][0]);
               opt1.setText(questionmcqm[currentmcqm][1]);
               opt2.setText(questionmcqm[currentmcqm][2]);
               opt3.setText(questionmcqm[currentmcqm][3]);
               opt4.setText(questionmcqm[currentmcqm][4]);
               opt1.setSelected(false);
               opt2.setSelected(false);
               opt3.setSelected(false);
               opt4.setSelected(false);        
           }
           else
           {
               question.setText("SECTION OVER!");
               opt1.setText("N/A");
               opt2.setText("N/A");
               opt3.setText("N/A");
               opt4.setText("N/A");
               //tt.stop();
               JOptionPane.showMessageDialog(null,"Section is over, click on END SECTION!");
               
           }             
    }//GEN-LAST:event_submitMouseClicked

